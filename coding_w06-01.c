#include <stdio.h>

int main() {
    // กำหนดค่าตัวแปรเริ่มต้น
    int a = 5, b = 17;
    float c = 8.5, d = 4.0;

    // บรรทัดที่ 1: แสดงค่าตัวแปรทั้งหมด
    printf("1. ค่าเริ่มต้น: a = %d, b = %d, c = %.2f, d = %.2f\n", a, b, c, d);

    // บรรทัดที่ 2: d + a = 4.0 + 5 = 9.0
    printf("2. d + a = %.2f\n", d + a); // ใช้ float + int ได้ → ผลลัพธ์ float

    // บรรทัดที่ 3: a - b = 5 - 17 = -12
    printf("3. a - b = %d\n", a - b);

    // บรรทัดที่ 4: c * d = 8.5 * 4.0 = 34.0
    printf("4. c * d = %.2f\n", c * d);

    // บรรทัดที่ 5: a * c = 5 * 8.5 = 42.5
    printf("5. a * c = %.2f\n", a * c);

    // บรรทัดที่ 6: c / d = 8.5 / 4.0 = 2.125
    printf("6. c / d = %.2f\n", c / d);

    // บรรทัดที่ 7: b / c = 17 / 8.5 = 2.0
    printf("7. b / c = %.2f\n", b / c);

    // บรรทัดที่ 8: a % b = 5 % 17 = 5 (เพราะ a < b)
    printf("8. a %% b = %d\n", a % b);

    // บรรทัดที่ 9: c % a → Error! เพราะใช้ % กับ float (c = 8.5)
    // วิธีแก้: แปลงเป็น int ก่อน
    printf("9. (int)c %% a = %d\n", ((int)c) % a); // 8 % 5 = 3

    // บรรทัดที่ 10: c % d → Error! เพราะ float % float ใช้ไม่ได้
    // วิธีแก้: แปลงทั้งสองเป็น int แล้วค่อย mod
    printf("10. (int)c %% (int)d = %d\n", ((int)c) % ((int)d)); // 8 % 4 = 0

    return 0;
}