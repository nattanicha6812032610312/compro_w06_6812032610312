#include <stdio.h>

int main() {
    // นิพจน์ที่ 1: ลำดับการคูณมากกว่าบวก
    int A = -2 + 5 * 2;  // 5 * 2 = 10 แล้ว -2 + 10 = 8
    printf("A = %d\n", A);  // แสดงค่า A = 8

    // นิพจน์ที่ 2: แบ่งก่อน คูณทีหลัง (จากซ้ายไปขวา)
    int B = 10 / 2 * 3;  // 10 / 2 = 5 แล้ว 5 * 3 = 15
    printf("B = %d\n", B);  // แสดงค่า B = 15

    // นิพจน์ที่ 3: ต้องหาค่าในวงเล็บก่อน
    int C = 6 / 2 + 3 * (4 % 2);  
    // 4 % 2 = 0 → 3 * 0 = 0 → 6 / 2 = 3 → 3 + 0 = 3
    printf("C = %d\n", C);  // แสดงค่า C = 3

    // นิพจน์ที่ 4: บวกในวงเล็บก่อน แล้วคูณ แล้วหารเอาเศษ
    int D = (5 + 2) * 15 % 4;
    // 5 + 2 = 7 → 7 * 15 = 105 → 105 % 4 = 1
    printf("D = %d\n", D);  // แสดงค่า D = 1

    // นิพจน์ที่ 5: คูณและหารก่อน แล้วค่อยบวก ลบ
    int E = 6 + 2 * 2 - 6 / 2;
    // 2 * 2 = 4, 6 / 2 = 3 → 6 + 4 - 3 = 7
    printf("E = %d\n", E);  // แสดงค่า E = 7

    // นิพจน์ที่ 6: มีทั้ง +, *, /, % ใช้ลำดับความสำคัญและวงเล็บ
    int F = 5 + 3 * 2 - 8 / 4 + (6 % 5);
    // 3 * 2 = 6, 8 / 4 = 2, 6 % 5 = 1
    // → 5 + 6 - 2 + 1 = 10
    printf("F = %d\n", F);  // แสดงค่า F = 10

    // นิพจน์ที่ 7: จัดลำดับวงเล็บก่อน แล้วคูณ/หาร ตามลำดับ
    int G = (4 + 3) * 2 - 10 / (2 + 3);
    // 4 + 3 = 7, 2 + 3 = 5 → 7 * 2 = 14, 10 / 5 = 2 → 14 - 2 = 12
    printf("G = %d\n", G);  // แสดงค่า G = 12

    return 0;
}
